package lambda表达式和匿名内部类的区别;

public class PtingString {
	private String name;
	private int age;
	public PtingString() {}
	public PtingString(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
